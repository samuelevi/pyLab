#### creating dictionary

countries : dict = {'ngr' : 'Nigeria',
                    'usa' : 'United States',
                    'uk' : 'United Kingdom',
                    'fr' : 'France',
                    'ger' : 'Germany'}
print(countries)


##### creating dictionary using dict()
countries : dict = dict(ngr = 'Nigeria',
                    usa = 'United States',
                    uk = 'United Kingdom',
                    fr = 'France',
                    ger = 'Germany')
print(countries)
